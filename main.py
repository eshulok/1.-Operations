#You can do math operations with Python
#Addition
print(5 + 2)

#Subraction
print(10 - 8)

#Mulitiplication
print(5 * 10)

#Division
print(10 / 2)

#The order of operations is important 
#Mulitiplication and division are higher order operations
#That means those operations are done first no matter what order they are written

#This will print 60 because multiplication comes first
print(10 + 5 * 10)

#Use parenthesis to change the default order of operations
#Operations inside the parenthesis are calculated first
#This will print 150
print((10 + 5) * 10)